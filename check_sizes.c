#include <stdio.h>
#include <stdint.h>

int main() {
    printf("sizeof(char): %zu\n", sizeof(char));
    printf("sizeof(int16_t): %zu\n", sizeof(int16_t));
    printf("sizeof(int32_t): %zu\n", sizeof(int32_t));
    printf("sizeof(int): %zu\n", sizeof(int));
    printf("sizeof(long): %zu\n", sizeof(long));
    return 0;
}
